// plus_one.cpp -- the increment oprator
#include<iostream>
int main()
{
    using namespace std;
    int a = 10;
    int b = 10;
    cout << "a = " << a << "; b = " << b << endl; 
    cout << "++a = " << ++a << "; b++ = " << b++ << endl;
    cout << "a = " << a << "; b = " << b << endl; 
    return 0;
}